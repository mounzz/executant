     */
    public function update(Request $request)
    {
            $user = Auth::user();
            $user->name = $request->name;
            $user->lastName = $request->lastName;
            $user->age = $request->age;
            $user->email = $request->email;
            $user->avatar_id = $request->avatar_id;
            $user->save();
            return redirect()->back();
    }

